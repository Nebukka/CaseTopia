export function rarityFromChance(chance: number): string {
  if (chance >= 50) return "common";
  if (chance >= 25) return "uncommon";
  if (chance >= 15) return "rare";
  if (chance >= 5) return "epic";
  if (chance > 1) return "mythic";
  return "legendary";
}
